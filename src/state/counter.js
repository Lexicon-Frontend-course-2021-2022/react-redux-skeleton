/* ============================================================================
 * Actions
 * ========================================================================= */
const actions = {
  increase: () => ({ type: 'INCREMENT' }),
  decrease: () => ({ type: 'DECREMENT' }),
  reset: () => ({ type: 'RESET' }),
}

/* ============================================================================
 * Reducer
 * ========================================================================= */
const reducer = (state = 0, action) => {

  switch (action.type) {
    case 'INCREMENT':
      return state + 1;

    case 'DECREMENT':
      return state - 1;

    case 'RESET':
      return 0;

    default:
      return state;
  }
}

/* ============================================================================
 * Exports
 * ========================================================================= */
export { actions, reducer };